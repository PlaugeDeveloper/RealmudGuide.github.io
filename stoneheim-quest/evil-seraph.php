<h1 class="text-center">Bones of Ogrimar</h1>

<p>
  Once you have both the Skull and Blood of Ogrimar, return to Sir Mordenthal and say "Bones of Ogrimar". He will tell you that you have only to ask the current holder of the Bones of Ogrimar and he will give them to you.
</p>

<p>
  Head to Stonecrest and talk to Julian (Keyword: "Bones of Ogrimar", "Yes", "Cavern", "Entrance"). He will tell you a story about a man named Venadar and that he found the Bones after falling in a hall. He will also tells you that he climbed out of the hall and traced his steps back to a well, which is located near Khimtesar's house (Khimtesar = Gateway teacher). Head to Khimtesar house and click on the well just outside of the house.

</p>

<p>
  Follow the directions (7 steps west, 20 steps north, the "drop spot" to the hall is exactly in front of the small rock) and you will end up in a small cave. Go down the stairs and head east. You will end up in another cave with a staircase leading down.
</p>

<p>
  Go down the stairs and head as far east as you can go. You will meet an NPC called "A Frightening Beast" (actually is Venadar). Say "Bones of Ogrimar", "Object", "Ring" and he will tell you that someone stole the Bones from him and that the thief left a ring behind. He will refuse to give you the ring, so kill him leave the screen and return. He will appear once again. Just click on him and he will give you an Ancient Ring.
</p>

<p>
  Go back to Sir Mordenthal and click on him. He will direct you to Drardos (located at Silversky town) to learn more about the ring. Head to Drardos and say "Ancient Ring ", "Yes" and he will send you to Brother Thorkas (located at Silversky temple).
</p>

<p>
  Say "Ancient Ring", "Yes", "Master Jeweler" to Thorkas and he will say that the ring is evil. Head back to Drardos and click on him. He will send you to Xanth (Blindman) in order to learn more about the ring. Head to Xanth and say "Ancient Ring", "Yes" to him and he will ask for: 1 Essence of Bloodlust (dropped by Bloodlust)
</p>

<p>
  1 Bloodstone Ring (dropped by Bloodlust or from the Bloodstone Ring quest) Find them and return to Xanth. Say "Ancient Ring" and he will tell you that the Ancient Ring belongs to Sir Mordenthal. Head to Mordenthal (or just double click on the ancient ring and you will be teleported to Mordenthal's castle) and say "Ancient Ring", "Give me the Bones" a few times.

</p>

<p>
  He will act as if he doesn't know what you are saying. Once he asks who told you, answer "Xanth" and then say again "Give me the Bones" and he will give them to you.
</p>

