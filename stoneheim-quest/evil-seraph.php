</p>


<h1 class="text-center">Oracle Zone</h1>

<p>
  First you must head to the Chamber of Providence. Go to Sunken Woods (near Gateway Teacher house) and search for two Monoliths/Spires. Go between them across a bridge of water. You will end up on a small island with a dungeon (Chamber of Providence). Go down the dungeon and find a portal. Go through the portal and you will end up in the Heart of Destiny (the center of Stoneheim). Search to find a whirlpool and go through it.

</p>


<p>
  You will end up at the Oracle Realm. Wander around until you find an ivory chest. In the chest you will find a Ivory Key. Once you have the key go to the Oracle's House and enter it. In this house you will find many puzzles, traps and mazes. Each area is a different puzzle to solve. After you have completed all of the puzzles and challenges you will get to meet the Oracle (NPC). Talk to him, take his final test, pass it and you will become a Seraph.
</p>

