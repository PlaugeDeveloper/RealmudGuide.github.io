  <p>
   Once you have the Key of Artherk or the Key of Ogrimar, head to the Chamber of Providence. To find the Chamber of Providence Go to Sunken Woods (near Gateway Teacher house) and search for two Monoliths/Spires. Go between them across a bridge of water. You will end up on a small island with a dungeon (Chamber of Providence).

  </p>
 
 <p>
   Go down the dungeon and find a portal. Go through the portal and you will end up in the Heart of Destiny (the center of Stoneheim). Search for a whirlpool and go through it. You will end up at the Oracle Realm. Wander around until you find an ivory chest (it is marked on the full map of Oracle Realm. In the chest you will find a Ivory Key. Once you have the key go to the Oracle's House and enter it (check the map below).
 </p>
 
  <p class="text-center"><img src="../images/maps/oracle-1.jpg" alt="" width="650" height="400"></p>

<p>
  Once you enter the Oracle house head to the C1 chest (check the map above) and grab the Crude Iron Key. Head back to the entrance room and open the door to the north. In this room you will find a lot of small corridors. At the end of every corridor you will find a chest. In one of those 11 chests (marked 1-11 with white letters) you will find the Dull Copper Key.
</p>        

<p>
  Back out of the room and head NE. Open the door at the end of the corridor (check the above map) and you will end up in a small maze with 8 chests "inlaid with precious stones". Every chest drops a key that opens another chest, until you find the Emerald Crystal Key. The row that you must follow it's marked on the above map with yellow letters, from 1 to 8.
</p>

<p>
  The next room has 2 doors (NW and W). The door at NW, it doesn't open so head to the door at your West. You will come to some rooms full of B'thonians. Kill them, until one drops the Shiny Silver Key. Grab it and head for the door that opens (check above map again).
</p>

<p>
  Open all the doors to the west and you will end up at a room full of B'thurkans. Kill them until they drop the Solid Gold Key. Get the key and open the door to the south. You will end up at a room with some teleporters. Head to the chest until you step on the first teleporter. Once you have teleported move backwards until you step on another teleporter.
</p>

<p>
  Then again move to the opposite direction until you hit the next teleporter. Do this (move back and forward) until you end up at the chest (marked C2 on the map above). Open it and take the Shimmering Platinum Key. This key opens the door out of the Oracle House. Leave the Oracle House (don't forget to check the chest behind the secret door (illusion wall)***). It's the chest marked with a green C at the map above.
</p>

<p>
  Note: This chest is not accessible in version 1.25a or beyond. The wall you need to walk through is solid so you cannot reach the chest.
</p>

<p class="text-center"><img src="../images/maps/oracle-2.jpg" alt="" width="650" height="400"></p>

<p>
  Walk around the house (the way is marked on the map above) and enter the portal. Follow the arrows and go down the stairs marked as D2. Note: Watch out for the Time Protectors. A nice way to go through those trees is the Sanctuary spell. If you haven't learned it then be sure to have a lot of potions in your backpack.
</p>
<p class="text-center"><img src="../images/maps/oracle-3.jpg" alt="" width="500" height="300"></p>
<p>
  You will end up at U2 spot marked on the map above. In front of you there is a chest. Open it and you will take the Small Gold Key. The door south leads to the famous 3 chests with the great spawns :-) The Small Gold Key opens the door to the North. There is another way to reach this point from the D1 stairs (marked on the previous map (near the Time Protectors battleground)) but if you choose the D1 way you will not have access at the famous 3 chests.</p>

<p>If you choose the D1 way you will end up at U1 spot (marked on the map). In front of you there is a chest. Open it and take the Small Silver Key, which opens the door to the south. Open the door at north or south (according to which way you had followed (D1 or D2)) and head west. Follow the corridor and you will end up in a room with two portals. The right portal for the Good Path and the left for Evil Path. Choose the portal according to your path and you will end up at the Chamber of Perseverance (right chambers for Good Path, left for Evil Path).
</p>
<p class="text-center"><img src="../images/maps/oracle-4.jpg" alt="" width="650" height="450"></p>

<p>
  This chamber has 6 rooms with Timeless Guardians. Kill them in the correct order (it's marked (1-6 with white letters) on the map above) and you gain 6 Oddly Shaped Shard of Stone. This is the key to the C1 chest (also marked on the map above). In the C1 chest you will find a Exquisitely Worked Stone Key which opens the door to the Chamber of Deception. In this chamber there are 7 chests (marked (1-7 with yellow letters) on the map above).</p>
<p>Only one chest (random) holds the key. The other ones are fire traps. Find the correct chest, grab the Scorched Metal Key and move out to the next chamber, the Chamber of Illusions. Guess what, the chamber of Illusions is full of illusion walls :-) The illusion walls are marked down on the map above with a black line. Find your way to the chest (marked as C2) and take the Translucent Key. Go through the door to the Chamber of Swiftness. Open the chest (marked as C3 on the map above) and grab the Enchanted Glass Key. Now head fast to the other chest (marked as C4) and open it to get the Pulsating Key. You must do it fast cause if you are late the key breaks. If it breaks head back to C3 chest and get another one.</p>

<p>
Once you have the Pulsating Key, unlock the door and head to Chamber of Despair. Head to the portal and enter it. You will end up in a room full with Oracle Gatekeepers. Watch out, cause only one of them can be killed. It's the only one that is separate (a little far away) from the others the moment you enter the room. Find it and kill it. It will drop the Gleaming Orichalcum Key. Grab it and head to the door.
</p>
<p>
Open it and head to the Stairs. Go down the stairs and move forward. At the end of the corridor turn left or right (according the path you have choose) and you will find a stair leading up. Go up and you will end up at a room with The Oracle (NPC). Talk to him "Work", "Balance", "One", "Makrsh P'tangh", "Potential", "Preperetions", "Test", "Key" and he will give you the Trial Key. Open the door to the North and go down the stairs.
</p>
<p>
Move forward and you will see a portal. Step on the portal and you will end up in a room with Gabriel Archonis (for the Good Path) or Gaenen Elthorn (for the Evil Path). In this room there are 4 doors. If you touch them you will be teleported at a town, so if you want to become a Seraph don't do it. Now it's time for your final test.
</p>
<p>
As Oracle said, you must face the death in order to past your final test. So strip (0 AC), don't cast any protection spells and attack Gabriel or Gaenen. Your char will miss until he is ready to die. Only then your char will do successful hits and kill him. Once he is dead you will be teleported back to The Oracle. Click on him and say "Purity of Spirit and Soul", "Reborn" "Ready to Reborn", "Yes" and...
</p>
