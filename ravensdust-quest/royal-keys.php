</ul>
<p>
  The last zombie (a Loathsome Horror) is the one you must give the 6 Royal Keys to so he can open the Royal Doors to the Lower Dungeon and get the Cuthana Herb. What follows is an extensive walkthrough of how to receive the 6 Royal Keys:
</p>
 
 <p><img src="../images/maps/12brothers-map.jpg" alt="" width="700"></p>
        

<h2 class="text-center">Royal Key #1</h2>

<p class="text-center"><img src="../images/key.png" alt="" width="100" height="100"></p>

<p>
  Find Celydia in the Silversky Palace and say "Royal Key" and "Reynen". Go to the Silversky Tavern, find Laren and say "Reynen" to him, too. He will reveal that he once had a relationship with Adriana and send you to Belagan (Adriana's assistant). Say "Reynen", "Yes" and he will ask for a Book of Warfare (you can buy one from Tobias in the Silversky Palace, near the Palace Dungeon, by saying "Book of Warfare" for 15.000 Gold Pieces).
</p>
<p>
  Take the book back to Belagan and say "Book of Warfare", "Reynen". Pay attention to what he says. He might ask for another book for Adriana (it seems that if you have done the Blade of Sharpness Quest, he doesn't) or send you to Reynen Aspicdart. Go to the Connector Cave and find Reynen (look at the map). Say "Royal Key" and he will tell you that it's in a chest that may be locked. Find the chest which is, of course, locked and return to Reynen. Say "Locked" and he will give you the key to it (Reynen Key). Go back to Reynen's Chest and open it.
</p>
<p>
  
Note #1: The chest drops the Royal Key in a completely random way. Keep trying until it does so.
Note #2: To do this quest you must have 100+ Karma. Consult the Karma Tips page for information on how to raise your karma.
</p>

<h2 class="text-center">Royal Key #2</h2>
<p class="text-center"><img src="../images/key.png" alt="" width="100" height="100"></p>
<p>
  In the Silversky Palace, find Terwin (he is near the throne). Say "Royal Key" and he will ask for a Potion of Heroism (Orc Battlemages drop it, consult the item chart for more information). Take one to him and say "Royal Key" again. He will tell you that Chamberlain Thomar has it (he is in the palace, too). Find him and say "Royal Key". He will ask you to become a Champion of Light first. Say "Champion of Light" and he will give you the key.

</p>
<p>
  Note: To do this quest you must have 100+ Karma. Consult the Karma Tips page for information on how to raise your karma.
</p>

<h2 class="text-center">Royal Key #3</h2>
<p class="text-center"><img src="../images/key.png" alt="" width="100" height="100"></p>

<p>
  In Silversky, find Elysana Blackrose (she is in a house south of the temple). Say "Royal Key" and she will ask for Ether. What she is looking for is a Flask of Bluish Liquid (Minotaurs drop it, consult the item chart for more information. Alternatively, you can get it from the Flask of Bluish Liquid quest in Arakas). Take it to her and say "Royal Key" again. She will ask if you want a Serious Heal Pot. Answer "No" and she will send you to Zhakar. Find him (at the RD Tower of Sorcery) and say "Elysana Blackrose" 3 Times.
</p>
<p>
  He will ask you to solve the following riddle: "Name three Keys that won't open doors". Answer "Donkey Monkey Turkey" and he will give you the key. Note #1: To do this quest you must have 100+ Karma. Consult the Karma Tips page for information on how to raise your karma. Note #2: You must be a Champion of Light in order to start this key.
</p>

<h2 class="text-center">Royal Key #4</h2>
<p class="text-center"><img src="../images/key.png" alt="" width="100" height="100"></p>

<p>
  Head for the Silversky Tavern and talk to Thea (she only talks at night). Say "Royal Key" and she will send you to Moonrock in the Lighthaven Temple. Say "Royal Key" to her and she'll tell you to find Bane Blackblood. In order for him to talk to you, you must have done the Audience to Bane Blackblood Quest. When you find him, say "Royal Key" many times until he gives it to you. Attention: When you say "Royal Key" he will attack you.

</p>
<p>
  Don't kill him. Just leave the castle and return and he will have... forgotten it :-). Keep trying until he gives you the key. he will attack you.

</p>
<p>
  Note: In order for Moonrock to talk to you, you must have 170+ Karma. Consult the Karma Tips page for information on how to raise your karma.
</p>


<h2 class="text-center">Royal Key #5</h2>
<p class="text-center"><img src="../images/key.png" alt="" width="100" height="100"></p>

<p>
  In the Silversky Palace, find the cook, Jon Hamhoo. Say "Royal Key" and he will explain (with a looooong story) that it probably ended up in the hands of two friends, Jalir and Kamar. The correct names are Kalir and Jamar (the two friends that wander the desert). Go to Kalir first (he is the one in the Desert Oasis) and say "Royal Key".

</p>
<p>
  He'll say that he knows nothing about it and give you a letter for his brother, Jamar. Go to Jamar (he is in Silversky) and say "Give Letter". He will give you a letter for Kalir. Go back to Kalir and say "Give Letter", "Royal Key". He will admit having lied to you and explain that he has given the key to Morindin Arrowmist (he is north of the Tarantulas Pond). Go to Arrowmist, say "Royal key" and he will give it to you. 

</p>
<p>
  Note #1: In order for Morindin Arrowmist to talk to you, you must have 170+ Karma. Consult the Karma Tips page for information on how to raise your karma.
</p>
<p>
  Note #2: Morindin Arrowmist won't talk to you during the night time.
</p>
<p>
  Note #3: Morindin Arrowmist won't give you the Royal Key #5 if your level is lower then 40, he will tell you to come back when you grow stronger.
</p>

<h2 class="text-center">Royal Key #6</h2>
<p class="text-center"><img src="../images/key.png" alt="" width="100" height="100"></p>

<p>
  In the Silversky Palace, find Tobias (he is near the Palace Dungeon entrance). Say "Royal Key" and he will send you to Celestina Waterbreeze in the RD Druid Camp. Find her and say "Royal Key" and she will ask you to answer sincerely on the following question: "Have you ever killed a Forest Guardian". Answer the question sincerely and she will send you to Mordrick Warstone (in the Silversky Palace).
</p>

<p>
  Say "Royal Key" to him and he will tell you the story about the Sword of Might. He will also ask you to return it to him. Go to the connector cave and find the magic well. Say "Make Mine Thy Might" and you will get the Sword of Might in your inventory. Take it back to Mordrick and say "Sword of Might". He will give you the key.

</p>
<p>
  Note #1: To get the Sword of Might from the magic well, you must have 170+ Karma. Consult the Karma Tips page for information on how to raise your karma.Note #2: You might have to say "Royal Key" again in order for Mordrick to give it to you.Note #3: You must be a Champion of Light in order to start this key.
</p>

<p>
  As soon as you gather the 6 keys, go to the RD Crypt Level 3 and find a Loathsome Horror. Say "I have the six keys" and he will teleport you in a small cave where you will meet Theopold's Specter. Say "Work", "Riddles" to him and he will ask the following questions:

</p>
<h2>Question Answer:</h2>
<h3>What is the center of balance? → (The letter a)</h3>
<h3>What happens once in a minute, twice in a moment, once during weekdays but never on a weekend and never ever during a hundred thousand years? → (The letter m)</h3>
<h3>AYAMS TOKEER ANNEED SOMELP → (I am stuck here and need some help)</h3>
<h3>One where none or two should be. Seeking out purity in the King's tree. → (Unicorn)</h3>
<h3>I am better than eternal life, I am worse than death, dead men eat me, if you eat me, you will die. → (Nothing)</h3>

<p>For each riddle correctly answered, you will get 10.000 Experience Points. When you have answered all of them, you will be teleported to a small cave with a chest. Clicking on the chest will place the Cuthana Herb into your inventory and you will be teleported east of the Tarantula Pond.</p>

<p>Go to the Silversky Palace and find Chamberlain Thomar. Say "Delilah", "Help", "Noble Gesture" "Yes" and he will tell you to give the Cuthana Herb to princess Delilah. Say "Cuthana" to her (+20 Karma) and she will thank you giving you the following choices as a reward:</p>

<h2>A Prayer: She prays to Artherk for your soul to be saved</h2>
<h3>→ Healing: She heals your wounds</h3>
<h3>→ Gold: She tells you that you are evil and asks you to make another choice. If you insist on choosing gold, she gives you 10.000 Gold Pieces and lowers your karma by -14.</h3>
<h3>→ Remove Balork's Mark: She removes the mark that Balork put on you when you killed him.</h3>

<p>
  In order to move on to Stoneheim, you must choose "Remove Balork's Mark". In order to get the Silver Feather from Delilah, tell her "Stoneheim", it will appear in your backpack. Before leaving, say "Oracle" to Delilah. As a reward, you will be able to learn Healing and Sanctuary from Bishop Crowbanner (as long as he is in remission he will teach the spells to anybody). Head for Zhakar and say "Ritual". He will ask for the following items in order to perform the ritual to allow you to go to Stoneheim...
</p>

<h2>Items To Complete Ritual</h2>
<h3>Skeleton Bone from Skeleton Warlocks</h3>
<h3>Flask of Goblin Blood from Goblin Subchief</h3>
<h3>Flask of Crystal Water from Dune Raider</h3>
<h3>Belladonna Berries from Greyleaf</h3>
<h3>Silver Feather already obtain from Princess Delilah</h3>
<h3>5000 Gold Pieces</h3>

<p>
  Also, consult the items chart for more information and drops. Once you have turned them in you will have to wait 1 hour before Zhakar will send you to Stoneheim.
</p>
